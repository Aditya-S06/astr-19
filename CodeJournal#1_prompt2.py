# Tags describing the type of number
flot = "Floating Point:"
inte = "Integer:"

# Initializing variables 
x = 5.12
y = 832.2
z = 3
c = 234

# Equations being done to the variables
sumOfFloat = x + y
diffOfInt = c - z
productOfFloat = x * y
productOfInt = z * c

# Result
print(f"Sum of {x} + {y} is equal to {flot}{sumOfFloat}")
print(f"Difference of {c} - {z} is equal to {inte}{diffOfInt}")
print(f"Product of a {flot}{productOfFloat}")
print(f"Product of {inte}{productOfInt}")